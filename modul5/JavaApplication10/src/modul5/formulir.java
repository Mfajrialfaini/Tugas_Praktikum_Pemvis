//    ini buat button search cak
    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        try {
            String sql;
            sql = "select * from mahasiswa where nim = '" + jTextFieldSearch.getText() + "'";
            java.sql.Connection conn = (Connection) koneksi.getKoneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            if (res.next()) {
                jTextFieldNim.setText(res.getString("Nim"));
                jTextFieldNama.setText(res.getString("Nama"));
                String kelamin = res.getString("Kelamin");
                if ("Laki-Laki".equals(kelamin)) {
                    jComboBoxKelamin.setSelectedIndex(0);
                }else {
                    jComboBoxKelamin.setSelectedIndex(1);
                }
                jTextFieldEmail.setText(res.getString("Email"));
            }
            System.out.println("Berhasil");
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonSearchActionPerformed
