    private void tblMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMahasiswaMouseClicked
        // TODO add your handling code here:
        int row = tblMahasiswa.getSelectedRow(); 
        
        if (row != -1) {
            jTextFieldNim.setText(tblMahasiswa.getValueAt(row, 1).toString());
            jTextFieldNama.setText(tblMahasiswa.getValueAt(row, 2).toString()); 
            String kelamin = tblMahasiswa.getValueAt(row, 3).toString();
            if ("Laki-Laki".equals(kelamin)) {
                jComboBoxKelamin.setSelectedIndex(0); 
            } else {
                jComboBoxKelamin.setSelectedIndex(1);
            }
            jTextFieldEmail.setText(tblMahasiswa.getValueAt(row, 4).toString());
        }
    }//GEN-LAST:event_tblMahasiswaMouseClicked

