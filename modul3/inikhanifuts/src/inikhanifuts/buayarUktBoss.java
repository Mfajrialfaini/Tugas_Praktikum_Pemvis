    private void jButtonHitungUktActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHitungUktActionPerformed
        // TODO add your handling code here:
//        int iniGajiAyah, iniGajiIbu, iniJumlahAnak, iniJumlahUkt;
       String GajiAyah = jTextFieldGajiAyah.getText();
        int iniGajiAyah = Integer.parseInt(GajiAyah);
        
        
       String GajiIbu = jTextFieldGajiIbu.getText();
        int iniGajiIbu = Integer.parseInt(GajiIbu);
        
        String jumlahAnak = jTextFieldJumlahAnak.getText();
       int iniJumlahAnak = Integer.parseInt(jumlahAnak);
      
        int TotalGaji = iniGajiIbu+iniGajiAyah;
        
        if (TotalGaji > 3000000 && iniJumlahAnak <= 3 ){
            iniJumlahUkt = 3000000;
        }
        
         if (TotalGaji > 3000000 && iniJumlahAnak > 3 ){
            iniJumlahUkt = 1750000;
        }
         if (TotalGaji < 3000000 && TotalGaji > 20000000 && iniJumlahAnak <= 3 ){
            iniJumlahUkt = 1500000;
        }
         
        if (TotalGaji < 2000000 && iniJumlahAnak > 3 ){
            iniJumlahUkt = 1500000;
        }
     
        jTextFieldGajiAyah.setText(GajiAyah);
        jTextFieldGajiIbu.setText(GajiIbu);
        jTextFieldJumlahUkt.setText(String.valueOf(iniJumlahUkt));
        
        
    }//GEN-LAST:event_jButtonHitungUktActionPerformed

